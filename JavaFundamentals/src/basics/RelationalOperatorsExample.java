package basics;

public class RelationalOperatorsExample {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 3;
        int num3 = 7;

        System.out.println("num1 > num2 : " + (num1 > num2));
        System.out.println("num1 < num2 : " + (num1 < num2));
        System.out.println("num1 == num3 : " + (num1 == num3));
        System.out.println("num1 != num3 : " + (num1 != num3));
        System.out.println("num1 >= num2 : " + (num1 >= num2));
        System.out.println("num1 <= num2 : " + (num1 <= num2));
        System.out.println();
    }
}
